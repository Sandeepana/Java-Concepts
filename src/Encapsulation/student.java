package Encapsulation;

public class student {

    private String name;
    private int age;

    public void setName(String name) {
        this.name = name;
    }

    public String getname() {
        return name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getAge() {
        return age;
    }

}